package uk.ac.aber.slj11;

public class Coordinate {
	private int x;
	private int y;
	
	Coordinate(int x , int y){
		this.x = x;
		this.y = y;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getX() {
		return x;
	}

	public void setY(int y) {
		this.y = y;
	}

	public int getY() {
		return y;
	}
	
	
}
